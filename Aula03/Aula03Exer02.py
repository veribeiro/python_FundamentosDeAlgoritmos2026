# Faça um programa que peça um valor numérico. Escreva na tela se o valor é positivo ou negativo.
# Observação: Resolva usando apenas Estruturas de Decisão Completas.

num = int(input("Digite um número "))

if num>0: 
    print("Positivo")

elif num<0:
    print("Negativo")

else:
    print("É zero")