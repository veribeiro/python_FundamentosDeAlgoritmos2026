# Faça um programa que leia um número. Exiba o dia correspondente da semana.
# Observação: Resolva usando apenas Estruturas de Decisão Simples

num = int(input("Digite um número "))

if num==1: 
    print(num, " - Domingo")

if num==2: 
    print(num, " - Segunda")

if num==3: 
    print(num, " - Terça")

if num==4: 
    print(num, " - Quarta")

if num==5: 
    print(num, " - Quinta")

if num==6: 
    print(num, " - Sexta")

if num==7: 
    print(num, " - Sábado")